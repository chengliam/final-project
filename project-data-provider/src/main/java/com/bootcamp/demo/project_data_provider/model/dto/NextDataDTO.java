package com.bootcamp.demo.project_data_provider.model.dto;

public class NextDataDTO {
  private int id;
  private String symbol;
  private Double c;
  private Double d;
  private Double dp;
  private Double h;
  private Double l;
  private Double o;
  private Double pc;
  private Long t;

  public NextDataDTO() {}

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getSymbol() {
    return symbol;
  }

  public void setSymbol(String symbol) {
    this.symbol = symbol;
  }

  public Double getC() {
    return c;
  }

  public void setC(Double c) {
    this.c = c;
  }

  public Double getD() {
    return d;
  }

  public void setD(Double d) {
    this.d = d;
  }

  public Double getDp() {
    return dp;
  }

  public void setDp(Double dp) {
    this.dp = dp;
  }

  public Double getH() {
    return h;
  }

  public void setH(Double h) {
    this.h = h;
  }

  public Double getL() {
    return l;
  }

  public void setL(Double l) {
    this.l = l;
  }

  public Double getO() {
    return o;
  }

  public void setO(Double o) {
    this.o = o;
  }

  public Double getPc() {
    return pc;
  }

  public void setPc(Double pc) {
    this.pc = pc;
  }

  public Long getT() {
    return t;
  }

  public void setT(Long t) {
    this.t = t;
  }
}
